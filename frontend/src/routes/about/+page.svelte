  <h1>About the Project</h1>
  <p>This project explores how recent changes in U.S. immigration policy under the current administration are affecting immigrant juveniles’ access to legal representation and the outcomes of their protection cases.</p>

  <h2>Problem Statement</h2>
  <p>Immigrant juveniles in the U.S. face serious barriers to legal representation, especially under recent changes in immigration policy. These policies have increased case backlogs, limited access to legal aid, and negatively affected outcomes for children seeking protection. Our group aims to understand how these systemic issues shaped by shifting policies, institutional structures, and public attitudes impact vulnerable youth and what can be done to improve fairness and access to justice.</p>

  <h2>Actionable Research Question</h2>
  <p>“How do changes in U.S. immigration policy under the current administration affect access to legal representation and outcomes for immigrant juveniles seeking protection?”</p>
