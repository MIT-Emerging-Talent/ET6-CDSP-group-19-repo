  <h1>Data & Analysis</h1>
  <p>This section presents the datasets, data preparation process, and the results of the data analysis, including visualizations.</p>

  <h2>Dataset Source</h2>
  <p>The Executive Office for Immigration Review (EOIR) Case Data from Data.gov is the primary source of information for this project.</p>

  <h2>Non-Technical Summary of the Data Analysis</h2>
  <p>This project investigates how legal representation and case results for juvenile immigrants have shifted in response to federal immigration policies enacted between 2018 and 2025. Results indicate the following:</p>
  <ul>
    <li>Juveniles with legal representation are more likely to receive favorable decisions (45.3% vs. 0.8% without representation).</li>
    <li>Major policy changes align with changes in representation rates.</li>
    <li>Demographic characteristics affect outcomes, but representation remains the most reliable predictor.</li>
  </ul>
